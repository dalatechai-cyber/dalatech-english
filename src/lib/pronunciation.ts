const DIFFICULT_PATTERNS = /th|[vwfrz]|ea|ou|ough|ie|oi/i

export function findDifficultWords(text: string): Array<{ word: string; ipa: string }> {
  const wordSet = new Set<string>()
  const matches = text.match(/\b[a-zA-Z]{3,}\b/g) || []
  const results: Array<{ word: string; ipa: string }> = []

  for (const raw of matches) {
    const lower = raw.toLowerCase()
    if (!wordSet.has(lower) && DIFFICULT_PATTERNS.test(lower) && IPA_DICT[lower]) {
      wordSet.add(lower)
      results.push({ word: lower, ipa: IPA_DICT[lower] })
    }
  }
  return results.slice(0, 8)
}

export function speakWord(word: string): void {
  if (typeof window === 'undefined' || !window.speechSynthesis) return
  const utterance = new SpeechSynthesisUtterance(word)
  utterance.lang = 'en-US'
  utterance.rate = 0.85
  window.speechSynthesis.cancel()
  window.speechSynthesis.speak(utterance)
}

export function speakText(text: string): void {
  if (typeof window === 'undefined' || !window.speechSynthesis) return
  const cleanText = text.replace(/<[^>]+>/g, '').replace(/[^\x00-\x7F]/g, ' ')
  const utterance = new SpeechSynthesisUtterance(cleanText)
  utterance.lang = 'en-US'
  utterance.rate = 0.85
  window.speechSynthesis.cancel()
  window.speechSynthesis.speak(utterance)
}
